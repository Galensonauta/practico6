package entidades;

/**
 *
 * @author y
 */
public class Categoria {
    private int orden;
    private String nombre;

    public Categoria() {
    }  

    
    public Categoria(int orden, String nombre) {
        this.orden = orden;
        this.nombre = nombre;
    }

    public int getOrden() {
        return orden;
    }

    public void setOrden(int orden) {
        this.orden = orden;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
}
